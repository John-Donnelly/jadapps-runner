/**
 * excel-hidden-sheet-destroyer: removes worksheets whose `state` is "hidden"
 * or "veryHidden". Auditors and analysts often want to confirm a workbook
 * has no concealed sheets before sharing.
 */

import { statSync } from "node:fs";
import { join } from "node:path";
import type { StepResult, FileRef } from "../types.js";

interface ToolContext {
  toolId: string;
  inputs: Record<string, unknown>;
  fileRefs: FileRef[];
  scratchDir: string;
  emitProgress(bytes: number): void;
}

export default async function excelHiddenSheetDestroyer(ctx: ToolContext): Promise<StepResult> {
  const start = Date.now();
  const ref = ctx.fileRefs[0];
  if (!ref) return errorResult("missing_input", "excel-hidden-sheet-destroyer requires one .xlsx input");

  let ExcelJS: typeof import("exceljs");
  try { ExcelJS = (await import("exceljs")).default as typeof import("exceljs"); }
  catch (err) { return errorResult("driver_missing", `exceljs not installed: ${(err as Error).message}`); }

  const inPath = join(ctx.scratchDir, ref.ref);
  const totalIn = sizeOrFallback(inPath, ref.bytes);
  const wb = new ExcelJS.Workbook();
  await wb.xlsx.readFile(inPath);
  ctx.emitProgress(totalIn);

  const removed: string[] = [];
  const toRemove = wb.worksheets.filter((s) => s.state === "hidden" || s.state === "veryHidden");
  for (const sheet of toRemove) {
    removed.push(sheet.name);
    wb.removeWorksheet(sheet.id);
  }
  if (wb.worksheets.length === 0) {
    return errorResult("would_be_empty", `removing all hidden sheets would leave the workbook empty (sheets: ${removed.join(", ")})`);
  }

  const outRef = `visible-${ref.ref}`;
  const outPath = join(ctx.scratchDir, outRef);
  await wb.xlsx.writeFile(outPath);
  const outBytes = sizeOrFallback(outPath, 0);

  return {
    ok: true,
    outputs: { removedSheetCount: removed.length, removedSheetNames: removed },
    fileRefs: [{ ref: outRef, bytes: outBytes, sha256: "", mime: "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet", filename: ref.filename ?? "visible.xlsx" }],
    bytesProcessed: totalIn,
    durationMs: Date.now() - start,
  };
}

function sizeOrFallback(path: string, fallback: number): number {
  try { return statSync(path).size; } catch { return fallback; }
}

function errorResult(code: string, message: string): StepResult {
  return { ok: false, outputs: {}, fileRefs: [], bytesProcessed: 0, durationMs: 0, error: { code, message } };
}
